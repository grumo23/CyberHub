    public void contarID(){
        try { 
                Conexiones.con = Conexiones.crear();
                
                String sqlCount = "SELECT COUNT(*) FROM videojuegos";
                Statement stmt = Conexiones.con.createStatement();
                ResultSet rs = stmt.executeQuery(sqlCount);
                rs.next();
                int rowCount = rs.getInt(1);
                rs.close();
                stmt.close();

                int nextId = rowCount + 1;
                id_juego.setText(String.valueOf(nextId));
        } catch (SQLException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error al obtener la ID.");
        }
    }


    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        JFileChooser fileChooser = new JFileChooser();
        fileChooser.setFileSelectionMode(JFileChooser.FILES_ONLY);
        fileChooser.setAcceptAllFileFilterUsed(false);
        fileChooser.addChoosableFileFilter(new javax.swing.filechooser.FileNameExtensionFilter("Archivos JPG", "jpg"));

        int returnValue = fileChooser.showOpenDialog(null);
        if (returnValue == JFileChooser.APPROVE_OPTION) {
            File archivoSeleccionado = fileChooser.getSelectedFile();
            String id = id_juego.getText();
            
            Path directorioDestino = Paths.get("src\\cyberhub\\imagenes\\");
            if (!Files.exists(directorioDestino)) {
                try {
                    Files.createDirectories(directorioDestino);
                } catch (IOException ex) {
                    ex.printStackTrace();
                    JOptionPane.showMessageDialog(this, "Error al crear el directorio.");
                    return;
                }
            }
            
            Path rutaDestino = directorioDestino.resolve(id + ".jpg");

            try {
                Files.copy(archivoSeleccionado.toPath(), rutaDestino);
                JOptionPane.showMessageDialog(this, "Imagen guardada.");
            } catch (IOException ex) {
                ex.printStackTrace();
                JOptionPane.showMessageDialog(this, "Error al guardar la imagen.");
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jLabel2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel2MouseClicked
        String nombre = tituloTexto.getText();
        String id = id_juego.getText();
        String lanzamientoStr = lanzamientoTexto.getText();
        String desarrolladora = desarrolladoraTexto.getText();
        String formato = (String) formatos.getSelectedItem();
        String p1 = (String) pl1.getSelectedItem();
        String p2 = (String) pl2.getSelectedItem();
        String p3 = (String) pl3.getSelectedItem();
        String p4 = (String) pl4.getSelectedItem();
        String p5 = (String) pl5.getSelectedItem();
        String p6 = (String) pl6.getSelectedItem();
        String descripcion = desc.getText();
        
        int lanzamiento = 0;
        try {
            lanzamiento = Integer.parseInt(lanzamientoStr);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "El lanzamiento debe ser un número entero.");
            return;
        }
        
        String plataformas = "";
        if (p1 != null && !p1.equals("null")) plataformas += p1 + ", ";
        if (p2 != null && !p2.equals("null")) plataformas += p2 + ", ";
        if (p3 != null && !p3.equals("null")) plataformas += p3 + ", ";
        if (p4 != null && !p4.equals("null")) plataformas += p4 + ", ";
        if (p5 != null && !p5.equals("null")) plataformas += p5 + ", ";
        if (p6 != null && !p6.equals("null")) plataformas += p6;
        
        if (!plataformas.isEmpty()) {
            plataformas = plataformas.substring(0, plataformas.length() - 2);
        }

        try {
            Conexiones.con = Conexiones.crear();
            
            String sqlInsertVideojuego = "INSERT INTO videojuegos (nombre, id, anio, desarrolladora, formato, plataformas) VALUES (?, ?, ?, ?, ?, ?)";
            PreparedStatement psInsertVideojuego = Conexiones.con.prepareStatement(sqlInsertVideojuego);
            psInsertVideojuego.setString(1, nombre);
            psInsertVideojuego.setString(2, id);
            psInsertVideojuego.setInt(3, lanzamiento);
            psInsertVideojuego.setString(4, desarrolladora);
            psInsertVideojuego.setString(5, formato);
            psInsertVideojuego.setString(6, plataformas);
            psInsertVideojuego.executeUpdate();
            psInsertVideojuego.close();
            
            String sqlInsertInformacion = "INSERT INTO informacion (id_juego, descripcion, P1, P2, P3, P4, P5, P6) VALUES (?, ?, ?, ?, ?, ?, ?, ?)";
            PreparedStatement psInsertInformacion = Conexiones.con.prepareStatement(sqlInsertInformacion);
            psInsertInformacion.setString(1, id);
            psInsertInformacion.setString(2, descripcion);
            psInsertInformacion.setString(3, p1);
            psInsertInformacion.setString(4, p2);
            psInsertInformacion.setString(5, p3);
            psInsertInformacion.setString(6, p4);
            psInsertInformacion.setString(7, p5);
            psInsertInformacion.setString(8, p6);
            psInsertInformacion.executeUpdate();
            psInsertInformacion.close();

            JOptionPane.showMessageDialog(this, "Registros insertados.");
        } catch (SQLException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error al insertar.");
        }
    }//GEN-LAST:event_jLabel2MouseClicked

